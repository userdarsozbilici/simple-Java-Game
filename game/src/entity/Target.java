package entity;

public class Target{
	public int targetX;
	public int targetY;
	public int targetHeight;
	public int targetWidth;
	public int targetSpeed;

	public Target(int x, int y, int height, int width, int speed) {
		this.targetX = x;
		this.targetY = y;
		this.targetHeight = height;
		this.targetWidth = width;
		this.targetSpeed = speed;
	}
}
